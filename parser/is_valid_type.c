#include "libftprintf.h"

int		is_valid_type(char c)
{
	if (c == 's' || c == 'd' || c == 'i' || c == 'c' || c == 'p' ||
			c == 'b' || c == 'o' || c == '%')
		return (TRUE);
	return (FALSE);
}
